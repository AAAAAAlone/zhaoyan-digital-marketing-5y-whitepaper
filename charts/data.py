def l1_year() -> dict:
    return json.loads((ROOT / "assets/data/manifest.json").read_text(encoding="utf-8"))["l1_year"]


def pain_share_series() -> dict[str, list[float]]:
    keys = ["SEO", "GEO", "官网", "销售"]
    out = {k: [] for k in keys}
    for y in YEARS:
        d = PAIN_YEAR.get(y, {})
        total = sum(d.values()) or 1
        for k in keys:
            out[k].append(round(d.get(k, 0) / total * 100, 2))
    return out


def industry_share_series() -> dict[str, list[float]]:
    exp = json.loads((ROOT / "assets/data/expand-stats.json").read_text(encoding="utf-8"))
    ind_map = {
        "SaaS/企业服务/软件": "SaaS",
        "制造/工业/工业互联网": "制造",
        "营销/广告/传媒/公关": "营销",
        "教育/培训": "教育",
        "出海/外贸": "出海",
    }
    ind_keys = list(ind_map.values())
    ind_series = {k: [] for k in ind_keys}
    for y in YEARS:
        row = {ind_map.get(n, n): v for n, v in exp["industries_by_year"].get(y, []) if n in ind_map}
        total = sum(row.values()) or 1
        for k in ind_keys:
            ind_series[k].append(round(row.get(k, 0) / total * 100, 2))
    return ind_series
